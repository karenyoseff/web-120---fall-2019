    <p>In these days, is hard to decide whether the companies and web developers need to build a mobile app or just used a responsive website.</p>
    <p>“Responsive mobile design generally means that your site is designed specifically for mobile use. It will often be housed under a different URL (usually something like m.yoursite.com) and will feature subsets of your desktop content that are appropriate for mobile viewing. These pages will adapt to whatever screen size the user desires. Mobile apps, on the other hand, are specific applications customized to each Operating System (OS) that feature unique designs and codebases. In our mobile-crazed world, native mobile apps are becoming a popular choice for many sites that want to offer users a top-shelf experience.”<sup>1</sup></p>
    <p>The idea is to have clear information on how the users are behaving and what are their necessities and what are the necessities of your business. The company Belitsoft published an article where states: “Statista claims, that 52.5 % of all website traffic worldwide in 2018 is generated through mobile phones.” where they try to inform how fast people are using their phones more than their desktops” they also said: “After almost a decade, apps hold a predominant position when it comes to the share of mobile time. More than 80% of mobile minutes worldwide are spent on apps.”<sup>2</sup></p>

    <p>For this here are some pros of each adaptation.</p>
    <p><b>Responsive website</b></p>
        <p><u> It doesn’t require downloading or installation.</u>Most of the sites are free. Is easy to use a responsive website on your smartphone. It needs internet connection.</p>
        <p><u>  The mobile website is less expensive.</u> You don’t have to create native apps for each type of mobile phones and tablets when you have one responsive website</p>
    <p><b>Mobile app</b></p>
        <p><u>Focusing on special features and functions. </u>Mobile application doesn’t have to fully duplicate features of the responsive site.</p>

        <p><u>Mobile apps work differently.</u>  Apps can guarantee bigger safety than browsers.</p>
        <p><u>Apps can work offline.</u> It can download information or content and use it even without Internet access.</p>
        <p><u>Special user-friendly design.</u> Design for a mobile app is typically more holistic and user-friendly as it is done for a specific platform. Pictures usually upload faster and the app itself uses fewer mobile data.</p>
        <p><u>Using of mobile phone components.</u> Improves acting with a device and makes user’s life easier.</p>
        
    <p>“Both alternatives have their advantages and disadvantages. A mobile app is not always better than the mobile version of the site and vice versa. To choose one of them you must understand the purpose of the product: who will use it and how often.”<sup>3</sup> It depends of the necessities of each company and their goals to decide which is the best option, because for some companies it might be a not cost able choice and not usefull to create a mobile app and keep with the responsive webpage.</p>



